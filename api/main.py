@dataclass(frozen=True)
class Settings:
    """Runtime settings for API security controls and execution defaults."""

    app_name: str
    app_version: str
    environment: str
    cors_origins: list[str]
    allowed_hosts: list[str]
    rate_limit_per_minute: int
    max_query_length: int
    default_max_sources: int
    api_key: str
    enforce_api_key_in_production: bool
    enable_hsts: bool


class InMemoryRateLimiter:
    """Sliding-window limiter used to protect research endpoints from abuse."""

    def __init__(self, window_seconds: int = 60) -> None:
        """Initialize in-memory limiter state."""

        self._window_seconds = window_seconds
        self._store: dict[str, deque[float]] = {}
        self._lock = Lock()

    def allow(self, key: str, limit: int) -> bool:
        """Return True when current request is within configured rate limit."""

        now = perf_counter()
        with self._lock:
            queue = self._store.setdefault(key, deque())
            cutoff = now - self._window_seconds
            while queue and queue[0] < cutoff:
                queue.popleft()
            if len(queue) >= limit:
                return False
            queue.append(now)
        return True

    def clear(self) -> None:
        """Reset in-memory limiter state for deterministic tests."""

        with self._lock:
            self._store.clear()


