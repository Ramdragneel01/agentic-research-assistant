def _validate_settings(runtime_settings: Settings) -> None:
    """Fail fast when runtime configuration is unsafe or invalid."""

    if runtime_settings.rate_limit_per_minute <= 0:
        raise RuntimeError("RATE_LIMIT_PER_MINUTE must be greater than 0")
    if runtime_settings.max_query_length < 64:
        raise RuntimeError("MAX_QUERY_LENGTH must be at least 64")
    if runtime_settings.default_max_sources < 1 or runtime_settings.default_max_sources > 8:
        raise RuntimeError("DEFAULT_MAX_SOURCES must be between 1 and 8")
    if not runtime_settings.cors_origins:
        raise RuntimeError("CORS_ORIGINS must include at least one origin")
    if not runtime_settings.allowed_hosts:
        raise RuntimeError("ALLOWED_HOSTS must include at least one host")

    if _is_production_env(runtime_settings.environment):
        if runtime_settings.enforce_api_key_in_production and not runtime_settings.api_key:
            raise RuntimeError("RESEARCH_API_KEY must be set when APP_ENV is production")
        if "*" in runtime_settings.cors_origins:
            raise RuntimeError("CORS_ORIGINS cannot include wildcard '*' when APP_ENV is production")
        if "*" in runtime_settings.allowed_hosts:
            raise RuntimeError("ALLOWED_HOSTS cannot include wildcard '*' when APP_ENV is production")


def _load_settings() -> Settings:
    """Read runtime settings from environment with safe defaults."""

    origins = os.getenv("CORS_ORIGINS", "http://127.0.0.1:4175,http://localhost:4175")
    allowed_hosts = os.getenv("ALLOWED_HOSTS", "127.0.0.1,localhost,testserver,api,frontend")
    return Settings(
        app_name=os.getenv("APP_NAME", "agentic-research-assistant"),
        app_version=os.getenv("APP_VERSION", "0.2.0"),
        environment=os.getenv("APP_ENV", "development"),
        cors_origins=[origin.strip() for origin in origins.split(",") if origin.strip()],
        allowed_hosts=[host.strip() for host in allowed_hosts.split(",") if host.strip()],
        rate_limit_per_minute=int(os.getenv("RATE_LIMIT_PER_MINUTE", "90")),
        max_query_length=int(os.getenv("MAX_QUERY_LENGTH", "800")),
        default_max_sources=int(os.getenv("DEFAULT_MAX_SOURCES", "5")),
        api_key=os.getenv("RESEARCH_API_KEY", "").strip(),
        enforce_api_key_in_production=_parse_bool(os.getenv("ENFORCE_API_KEY_IN_PRODUCTION"), default=True),
        enable_hsts=_parse_bool(os.getenv("ENABLE_HSTS"), default=False),
    )


def _require_api_key(
    x_api_key: Annotated[str | None, Header(alias="X-API-Key")] = None,
) -> None:
    """Enforce optional API key auth for protected research endpoints."""

    if not settings.api_key:
        return
    if x_api_key != settings.api_key:
        raise HTTPException(status_code=401, detail="Unauthorized")


def _guard_query(query: str, max_query_length: int) -> str:
    """Validate and sanitize user query for safe agent execution."""

    sanitized = re.sub(r"\s+", " ", query).strip()
    if len(sanitized) < 3:
        raise HTTPException(status_code=422, detail="Query must contain at least 3 characters")
    if len(sanitized) > max_query_length:
        raise HTTPException(status_code=422, detail=f"Query exceeds max length {max_query_length}")

    blocked_patterns = [r"ignore\s+previous\s+instructions", r"system\s+prompt", r"bypass\s+guardrails"]
    if any(re.search(pattern, sanitized, flags=re.IGNORECASE) for pattern in blocked_patterns):
        raise HTTPException(status_code=400, detail="Query contains disallowed instruction pattern")

    return sanitized


def _request_key(request: Request) -> str:
    """Build a rate-limit key from request client information."""

    return request.client.host if request.client else "unknown"


settings = _load_settings()
_validate_settings(settings)
compiled_graph = build_graph()
limiter = InMemoryRateLimiter(window_seconds=60)
AuthDep = Annotated[None, Depends(_require_api_key)]

